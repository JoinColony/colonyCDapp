import React from 'react';
import type { Meta, StoryObj } from '@storybook/react';

import NotificationBanner from '~common/Extensions/NotificationBanner';
import { useCopyToClipboard } from '~hooks/useCopyToClipboard';

const meta: Meta<typeof NotificationBanner> = {
  title: 'Common/Notification Banner',
  component: NotificationBanner,
  argTypes: {
    isAlt: {
      name: 'Is alt?',
      control: {
        type: 'boolean',
      },
    },
  },
  args: {
    isAlt: false,
  },
};

export default meta;
type Story = StoryObj<typeof NotificationBanner>;

const NotificationBannerWithHooks = (args) => {
  const { handleClipboardCopy } = useCopyToClipboard();

  return (
    <NotificationBanner
      {...args}
      handleClipboardCopy={() => handleClipboardCopy('text')}
    />
  );
};

export const Success: Story = {
  render: (args) => (
    <NotificationBannerWithHooks
      {...args}
      status="success"
      title="The required permissions have been updated. You can now enable the extension."
      action={{ type: 'call-to-action', actionText: 'Enable extension' }}
    />
  ),
};

